# This program will convert degrees celsius to degrees fahrenheit

# User greeting
print('welcome to degrees celsius and degrees fahrenheit program')
print('please enter temperature in degrees celcius;you may use decimals')
print()

# Get user input
celsius = float(input('Enter a temperature in degrees celsius: '))
                
# Convert degrees celsius to fahrenheit
fahrenheit = (celsius * 9 / 5) + 32


# Display result
print(celsius,'degrees celsius is equal to',fahrenheit,'degrees fahrenheit')              
print()

#exit message
print('Have a great day!')
